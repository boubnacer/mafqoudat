// Reactive translation hook that uses the language context
export const useTranslation = () => {
  const { currentLanguage } = useLanguage();
  
  const t = (key, params = {}) => {
    let translation = translations[currentLanguage]?.[key] || translations.en?.[key] || key;
    
    // Replace placeholders with actual values
    Object.keys(params).forEach(paramKey => {
      const placeholder = `{${paramKey}}`;
      translation = translation.replace(new RegExp(placeholder, 'g'), params[paramKey]);
    });
    
    return translation;
  };
  
  const tWithFallback = (key, fallback) => {
    return translations[currentLanguage]?.[key] || translations.en?.[key] || fallback;
  };
  
  return { t, tWithFallback, currentLanguage };
}; 